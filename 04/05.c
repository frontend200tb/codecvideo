#include <stdio.h>

int main()
{
  printf("5 + 5 = %d\n", 5 + 5);
  printf("10 - 5 = %d\n", 10 + 5);
  printf("2 * 2 = %d\n", 2 * 2);
  printf("9 / 3 = %d\n", 9 / 3);
  printf("9 / 2 = %d\n", 9 / 2);
  printf("9 %% 2 = %d\n", 9 % 2);
  printf("8 %% 2 = %d\n", 8 % 2);

  return 0;
}

