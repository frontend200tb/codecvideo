#include <stdio.h>

int main()
{
  char a[2][3]= {
    {'A', 'B', 'C'},
    {'a', 'b', 'c'}
  }; // двумерный массив

  printf("%c %c %c\n", a[0][0], a[0][1], a[0][2]);
  printf("%c %c %c\n", a[1][0], a[1][1], a[1][2]);

  int b[2][3]= {
    {10, 20, 30},
    {15, 25, 35}
  }; // двумерный массив

  printf("%d %d %d\n", b[0][0], b[0][1], b[0][2]);
  printf("%d %d %d\n", b[1][0], b[1][1], b[1][2]);

  return 0;
}

